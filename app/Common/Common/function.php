function iconvstr($str)
{
        $encode = mb_detect_encoding($str, array('ASCII','UTF-8', 'GB2312', 'GBK','BIG5'));
        //echo $encode;die;
        if ($encode != "UTF-8") {  //所有不是UTF-8的全部当GB2312转换,不是很严谨
            $str = iconv("GB2312","UTF-8",$str);
        }
        return $str;
}

function is_member_login(){
    $member = session('member_auth');
    if (empty($member)) {
        return 0;
    } else {
        return session('member_auth_sign') == data_auth_sign($member) ? $member['uid'] : 0;
    }
}

